package BasicMove;

/**
 * The class is used to exchange data among classes
 * @fileName DataExchange
 * @author Yu Hong
 *
 */
public class DataExchange {
	private boolean dangerous = false;
	
	/**
	 * the constructor of the class
	 */
	public DataExchange(){
		
	}
	
	/**
	 * to set the danger status
	 * @param status a boolean value indicating whether it is 
	 * dangerous or not
	 */
	public void setDangerStatus(boolean status){
		dangerous = status;
	}
	
	/**
	 * to get the danger status
	 * @return a boolean value indicating whether it is dangerous  
	 */
	public boolean getDangerStatus(){
		return dangerous;
	}
}
