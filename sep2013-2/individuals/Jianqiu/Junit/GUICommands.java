
public class GUICommands {
	
	private final int STOP =0;
	private final int FORWARD =1;
	private final int BACKWARD =2;
	private final int TURNLEFT =3;
	private final int TURNRIGHT =4;
	private final int BATTERYINFO =12;
	private final int MARK =20;
	private final int DISCONNECT =-1;
	
	
	public int stop(){
		return STOP;
	}
	public int forward(){
		return FORWARD;
	}
	public int backward(){
		return BACKWARD;
	}
	public int turnleft(){
		return TURNLEFT;
	}
	public int turnright(){
		return TURNRIGHT;
	}
	public int run(){
		return BATTERYINFO ;
	}
	public int markClosure(){
		
		return MARK;
	}
	public int disconncet(){
		
		return DISCONNECT;
	}
}
